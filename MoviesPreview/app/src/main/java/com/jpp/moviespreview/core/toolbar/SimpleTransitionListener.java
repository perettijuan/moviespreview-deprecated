package com.jpp.moviespreview.core.toolbar;

import android.support.transition.Transition;

/**
 * Helper Transition.TransitionListener definition to avoid inner interfaces.
 * <p>
 * Created by jpperetti on 18/12/16.
 */
public class SimpleTransitionListener implements Transition.TransitionListener {
    @Override
    public void onTransitionStart(Transition transition) {
        // do nothing
    }

    @Override
    public void onTransitionPause(Transition transition) {
        // do nothing
    }

    @Override
    public void onTransitionResume(Transition transition) {
        // do nothing
    }

    @Override
    public void onTransitionEnd(Transition transition) {
        // do nothing
    }

    @Override
    public void onTransitionCancel(Transition transition) {
        // do nothing
    }
}